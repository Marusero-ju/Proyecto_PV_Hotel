package ar.edu.unju.edm.model;

public class Roles {

	private String nombre;

		
	public Roles() {
		super();
	}

	public Roles(String nombre) {
		super();
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
}
