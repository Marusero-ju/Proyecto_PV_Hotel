package ar.edu.unju.edm.model;

public class Servicio {
	
	private String nombre;

		
	public Servicio() {
		super();
	}

	public Servicio(String nombre) {
		super();
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	
}
